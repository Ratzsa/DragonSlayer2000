uint16_t getSpriteLine(uint8_t category, uint8_t sprite, uint8_t row)
{
    uint8_t rowToReturn;
    switch(category)
    {
        case 0:
            rowToReturn = fullSquare[sprite][row];
            break;

        case 1:
            rowToReturn = trees[sprite][row];
            break;

        case 2:
            rowToReturn = houses[sprite][row];
            break;

        case 3:
            rowToReturn = tools[sprite][row];
            break;

        case 4:
            rowToReturn = hero[sprite][row];
            break;

        case 5:
            rowToReturn = monster[sprite][row];
            break;

        case 6:
            rowToReturn = tools[sprite][row];
            break;

        case 7:
            rowToReturn = tools[sprite][row];
            break;

        case 8:
            rowToReturn = tools[sprite][row];
            break;

        case 11:
            rowToReturn = fullSquare[sprite][row];
            break;

        default:
            exit(1);
            break;
    }
    return rowToReturn;
}